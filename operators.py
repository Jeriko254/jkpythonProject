#Arithmetic operators
num1=34
num2=10

print(num1+num2)
print(num1-num2)
print(num1*num2)
print(num1/num2)
print(num1%num2)#modulus-returns the remainder

#comparison operators
print(num1<num2)
print(num1>num2)
print(num1<=num2)
print(num1>=num2)
print(num1 == num2)#equal to
print(num1!=num2)#not equal to

#assignment operators
number=12
print(number)

number += 2
print(number)

b=56
